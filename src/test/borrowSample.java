    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Kasesesec","root","");
           String sql = "select Bookid,Title from newbooks where Bookid=?";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1,txtsearch.getText());
           ResultSet rs = pst.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel)borrowtable.getModel();
            while(rs.next()){
                model.addRow(new String[]{rs.getString(1),rs.getString(2)});
                
            }
        }
        catch(Exception ex)
        {
            //System.out.println("Error : "+ex.getMessage());
            JOptionPane.showMessageDialog(null,"Error Occured while trying to Process your Request");
            
            
            
        }

    }//GEN-LAST:event_btnsearchActionPerformed

