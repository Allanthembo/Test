    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try
        {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Kasesesec","root","");
           String sql = "INSERT INTO borrowbook VALUES (?,?,?,?,?,?)";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1,txtnumber.getText());
           pst.setString(2,txttitle.getText());
           pst.setString(3,txtborrower.getText());
           String dater = ((JTextField)issue.getDateEditor().getUiComponent()).getText();
           pst.setString(4,dater);
           String returnda = ((JTextField)returndate.getDateEditor().getUiComponent()).getText();
           pst.setString(5,returnda);
           pst.setString(6,txtauthority.getText());
           
           
           pst.executeUpdate();
           JOptionPane.showMessageDialog(this,"You have Successfully issued out a textbook to a Student/Teacher. Thank you");
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"You already given our the book. Contact the admin for more information");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

